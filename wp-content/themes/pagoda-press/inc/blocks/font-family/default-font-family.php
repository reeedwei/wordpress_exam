<?php

/* Default Font Family */
function pagoda_press_get_default_site_identity_font_family() {    
    return esc_html__( "Lora", 'pagoda-press' );
}

function pagoda_press_get_default_main_font_family() {    
    return esc_html__( "Poppins", 'pagoda-press' );
}

function pagoda_press_get_default_secondary_font_family() {    
    return esc_html__( "Lora", 'pagoda-press' );
}